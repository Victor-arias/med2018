<h1>¿Cómo participar?</h1>

<h2>¿Quiénes participan?</h2>
<p>Si tienes de 10 a 14 años y estudias en una institución educativa de Medellín puedes participar en el concurso que te llevará a Suiza con un acompañante para ser de los primeros en saber si Medellín será la sede de los Juegos Olímpicos de la Juventud 2018.</p>

<h2>¿Cómo entro a participar?</h2>

<p>¡Es muy fácil! </p>

<p>Entra a <?php echo CHtml::link('www.concursomedellin2018.com', CHtml::normalizeUrl('http://www.concursomedellin2018.com') ); ?></p>

<p>Lo primero que debes hacer es contarnos algunas cosas sobre ti y un adulto responsable que cuide de ti: </p>

<ul>
	<li>Te pediremos un correo electrónico y una contraseña que debes recordar porque es muy importante para que puedas entrar a jugar ¡que no se te olvide!.</li>
	<li>Te preguntaremos cómo te llamas, tu número de tarjeta de identidad, cuántos años tienes y si eres niño o niña. </li>
	<li>También pon el nombre de la persona adulta que cuida de ti, cuál es su nombre, su correo electrónico, su número de teléfono, dónde vive y qué relación tiene contigo.</li>
</ul>

<p>Dale clic al botón que dice “registrarse” y ¡listo! ya estás inscrito y puedes comenzar a jugar. </p>

<h2>Prepara tus conocimientos (3 ejes temáticos)</h2>

<p>En el juego pondremos a prueba tus conocimientos con preguntas sobre temas que seguro conoces:</p>

<ul>
	<li>¿Qué tanto sabes de Medellín?</li>
	<li>¿Qué haces tú por la convivencia de la ciudad?</li>
	<li>¿Cuánto sabes de deporte y de los Juegos Olímpicos de la Juventud?</li>
</ul>

<p>A medida que vas avanzando en el juego y subes de nivel, las preguntas serán más difíciles, pero seguro tu sabes las respuestas y te va a ir muy bien.</p>

<h2>Mecánica del juego</h2>

<p>Si ya estás inscrito, tendrás cada día dos oportunidades de participar, entre más días juegues, más oportunidades tendrás de ganar puntos y avanzar en el juego.</p>

<p>Será un reto de conocimiento y de rapidez, te haremos preguntas que debes responder antes de que se te acabe el tiempo. A medida que vas respondiendo correctamente los retos, irás ganando puntos y avanzando de nivel.</p>

<p>Son 5 niveles, cada uno tiene cuatro preguntas en total. Mira los puntos que ganarás por responder cada pregunta en los diferentes niveles. </p>
<ul>
	<li>Nivel 1: 10 puntos.</li>
	<li>Nivel 2: 30 puntos.</li>
	<li>Nivel 3: 50 puntos.</li>
	<li>Nivel 4: 100 puntos.</li>
	<li>Nivel 5: 200 puntos.</li>
</ul>

<p>No te preocupes si en algún momento no sabes la respuesta a alguna pregunta, pues tendrás varias ayudas durante el juego, pero utilízalas con cuidado porque sólo se activarán una vez por día: </p>
<ul>
	<li>10 segundos más</li>
	<li>eliminar 2 respuestas erradas </li>
	<li>pasar a la siguiente pregunta</li>
</ul>

<p>Las ayudas las tendrás cada ronda de juego, o sea cada día que juegues. </p>

<p>Día a día acumularás más puntos y si logras acumular bastantes te verás de primero en la tabla de posiciones junto a los mejores. Tienes hasta el 10 de junio ¡no pares de jugar! </p>

<h2>Ganadores</h2>

<p>El niño y la niña que obtenga la mayor cantidad de puntos acumulados y sea el primero en la tabla de posiciones ¡Se irá para Suiza!</p>

<h2>Premio </h2>

<p>El premio es un viaje a Suiza para el niño y la niña ganadores y un acompañante mayor de edad para cada uno, ellos serán una de las primeras personas en saber si Medellín será la sede de los Juegos Olímpicos de la Juventud 2018 ¡Vamos por el sueño olímpico!</br>Puedes conocer que incluye el premio en este enlace: <a href="http://www.concursomedellin2018.com/premio">¿Qué te puedes ganar?</a>
